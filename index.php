					<h2>Cartão de apresentação</h2>
					<h3>O que é uma carta de motivação e para que serve?</h3>
					<p>Uma carta de motivação é um texto de uma página que explica de forma resumida por que você é o candidato ideal para determinada vaga. Normalmente, o documento é um tipo de introdução que acompanha o currículo ou, no caso de bolsas de estudo e cursos internacionais, a ficha de inscrição.

						É importante não confundir a carta de motivação com a carta de apresentação, que apenas destaca informações específicas do currículo que correspondem aos requisitos da vaga a que você quer se candidatar.

						A carta motivacional vai além da carta de apresentação porque apresenta argumentos para ser escolhido pela empresa ou instituição. A ideia, então, é que você demonstre esse entusiasmo no texto.</p>
					<h3>Como criar uma carta de motivação?</h3>
					<p>A carta de motivação deve ter uma página, no máximo. No texto, você deve se apresentar, resumir seu histórico e dizer por que tem interesse na vaga ou na organização a que está se candidatando. </p>
					<h3>Estrutura da carta de apresentação</h3>
					<p>Sua carta deve ter pelo menos três parágrafos (escreva mais se você tiver mais informações para incluir). A ideia é que ela seja composta por três partes principais: </p>
					<ul>
					<li>Introdução: é onde você diz quem é, o que faz e por que está se candidatando. </li>
					<li>Parte principal: nesta parte você “vende seu peixe”, contando suas realizações, falando sobre suas competências e habilidades e explicando seu entusiasmo com a oportunidade. Sempre que possível, use dados que comprovem o que você está dizendo.</li>
					<li>Conclusão: nesta parte final você diz de que forma acredita que pode contribuir com a empresa ou área de estudo e termina apontando seu objetivo principal (fazer parte da equipe, por exemplo). Depois, basta agradecer a atenção de quem estiver lendo o texto. </li>
					</ul>
